        <h2>Repairing connector modules for Dummies</h2>
        <p>Welcome dear Engineer.
        You have reached the repair manual for the standard MK2 connector module.</p>
        <ul>
        <li><p>First, make sure the module is turned off.</p>
        <p>This is done by closing all three switch covers on the front top side.
        Make sure to wait for the generators to spin down before opening the protective cover.</p>
        <p class="warning">NOTE: Repairing the module while active is dangerous and voids the warranty.</p>
        </li>
        <li><p>Remove the protective cover.</p>
        <p>The cover is held on by four quickrelease clips.
        These can be opened with a simple pulling motion.
        Make sure to release all four clips before removing the access cover.
        The cover should release easily. Do not use excessive force.</p>
        </li> 
        <li><p>Inspect the cables to see if there are any loose connections.</p>
        <p>There should be ten connections.
        On both lefthand and righthand sides, there are ten rows of sockets.
        In the standard configuration, each lefthand row should be connected to a corresponding righthand row.</p>
        <p>NOTE: Different configurations are possible, but are not officially supported by the manufacturer.</p>
        </li>
        <li><p>In the case of misaligned or missing connections, the central display hub will show which connections need to be repaced or realigned.</p>
        <p>In the case that the central display does not show the relevant information,
        simply disconnect/reconnect one of the cables.
        This should trigger the repairs submodule to come online.
        There should be no danger as long as the module is properly powered down.</p>
        <p>Be aware that this display will differ significantly depending on the firmware and other external factors.</p>
        </li>
        <li>There are two modes currently known:
        <ol>
            <li><p>Position mode<p>
            <p>In this mode, the inner ring will show which color socket is currently connected.
            The outer socket will show the correct alignment color.
            If the connection is correct, both will light up white.</p>
            <p>These colors are in the same ordering as the rows of sockets.
            However, it is possible that this is not the case.
            We have not yet discovered the cause of this issue.</p>
            </li>
            <li><p>Color balance mode</p>
            <p>The central display will show four gauges of different colors.
            To realign the connections, make sure that each of these gauges is aligned in the center.
            The level of each gauge is directly correlated to the number of connections in the corresponding color.</p>
            <p>Please note that there are also black connectors, with no corresponding gauge.</p>
            </li>
        </ol>
        <p>If all connections are properly aligned, there will be a short reboot sequence,
        after which the modue is ready for use.</p>
        </li>
        <li><p>Replace the protective cover.</p>
        <p>Seat the cover on the module, making sure that no cables are caught between the edge.
        Pull each quickrelease snap open and place the loop on the corresponding hook.</p>
        <p>The snaps should close easily.  DO not use excessive force to close the snaps.</p>
        </li>
        <li><p>Restart the modue</p>
        <p>Open the tree switch covers one by one, and turn on each switch in sequence.</p>
        </li>
        </ul>
